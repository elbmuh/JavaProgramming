package exercise;

public class Ex_01 {
	public static void main(String[] args) {
		int aa = 50;
		double bb = 5.0;
		
		System.out.println(aa + "+" + bb + "=" + (int)(aa+bb));
		System.out.println(aa + "-" + bb + "=" + (int)(aa-bb));
		System.out.println(aa + "*" + bb + "=" + (int)(aa*bb));
		System.out.println(aa + "/" + bb + "=" + (int)(aa/bb));
	}
}
